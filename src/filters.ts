const POLITICAL_RE = new RegExp(
  POLITICAL_KEYWORDS.map((k) => k.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")).join("|"),
  "i"
);

export function hasText(message: Message): boolean {
  return (message.text ?? message.caption ?? "").trim().length > 0;
}

export function extractText(message: Message): string {
  return (message.text ?? message.caption ?? "").trim();
}

export function isPolitical(text: string): boolean {
  return POLITICAL_RE.test(text);
}
